print('Ինչպես է ձեր տրամադրությունը')
x = input()
if 'լավ' in x or 'հիանալի' in x and 'վատ' not in x:
    print('Հրաշալի է, ինձ մոտ նույնպես ամեն ինչ լավ է։')
elif 'վատ' in x and 'լավ' not in x:
    print('Ոչինչ, շուտով ամեն ինչ լավ կլինի։')
elif 'ոչ' in x or '?' in x:
    print('Ես ձեզ չհասկացա խնդրում եմ նորից պատասխանեք։')
else:
    print('Ես ձեզ չհասկացա խնդրում եմ նորից պատասխանեք։')